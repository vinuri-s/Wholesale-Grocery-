    private void txtIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIdActionPerformed
        String id = txtId.getText();
        String SQL = "Select * From Customer where id='" + id + "'";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/ThogaKade", "root", "mysql");
            Statement stm = connection.createStatement();
            ResultSet rst = stm.executeQuery(SQL);
            if(rst.next()){
                txtName.setText(rst.getString("name"));
                txtAddress.setText(rst.getString("address"));
                txtSalary.setText(rst.getString("salary"));
            }else{
                JOptionPane.showMessageDialog(this, "No customer found..");   
            }

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver s/w not found..");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_txtIdActionPerformed

